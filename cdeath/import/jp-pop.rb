ARGV.each do |file|
    $firstflag = true
    csvtext = ''
    year = month = 0
    date = nil

    year1 = month1 = year2 = month2 = 0
    $flipflag = false
    File.foreach(file) do |line|
        next if line =~ /外国人人口/ # 2025-04.csv から
        line = line.tr('０-９', '0-9').gsub(/\(/, '（').gsub(/\)/, '）').gsub(/〜/, '～')

            line.sub(/平成(\d+)年(\d+)月(\d+)日現在.*平成(\d+)年(\d+)月(\d+)日現在/) do
                $flipflag = true
                year2 = ($1.to_i + 1988).to_s
                month2 = $2
                year1 = ($4.to_i + 1988).to_s
                month1 = $5
            end
            STDERR.puts "#{file}: #{$1}:#{year1}-#{month1}, #{$4}:#{year2}-#{month2}"
        elsif line =~ /平成(\d+)年(\d+)月(\d+)日現在.*概算値.*平成(\d+)年(\d+)月(\d+)日現在/
            line.sub(/平成(\d+)年(\d+)月(\d+)日現在.*平成(\d+)年(\d+)月(\d+)日現在/) do
                year1 = ($1.to_i + 1988).to_s
                month1 = $2
                year2 = ($4.to_i + 1988).to_s
                month2 = $5
            end
            STDERR.puts "#{file}: #{$1}:#{year1}-#{month1}, #{$4}:#{year2}-#{month2}"
        elsif line =~ /(\d+)年(\d+)月(\d+)日現在.*\"(\d+)年(\d+)月(\d+)日現在/
            line.sub(/(\d+)年(\d+)月(\d+)日現在.*\"(\d+)年(\d+)月(\d+)日現在/) do
                year1 = $1
                month1 = $2
                year2 = $4
                month2 = $5
            end
            STDERR.puts "#{file}: #{$1}:#{year1}-#{month1}, #{$4}:#{year2}-#{month2}"
        elsif line =~ /(\d+)年(\d+)月1日現在|^時間軸コード/
            csvtext += line
        elsif line =~ /総   数| ～ |100歳以上.*and over|85歳以上.*and over/
            if $firstflag
                csvtext += "人口区分,時点,年齢階級,区分,単位,男女計,男,女\n"
                $firstflag = false
            end

            #"総   数",,Total,12571,6118,6453,,125836,61226,64610,123352,59991,63361
            #    |
            #    V
            #総人口,2023年11月1日現在（概算値）,総数,人口,万人,12431,6047,6384
            items =  line.split(/,/)
            age = items[0].gsub(/ |　/,'').gsub(/"/,'')
            if age =~ /0～4歳/
                age = '0歳～4歳'
            elsif age !~ /総数/ && age !~ /歳/
                age.gsub!(/(\d+)/, '\1歳')
            end
            #puts "#{items[0]}: #{age}"
            if ! $flipflag
                csvtext += "総人口,#{year1}年#{month1}月1日現在（概算値）,#{age},人口,万人,#{items[3]},#{items[4]},#{items[5]}\n"
                csvtext += "総人口,#{year2}年#{month2}月1日現在（確定値）,#{age},人口,千人,#{items[7]},#{items[8]},#{items[9]}\n"
                csvtext += "日本人人口,#{year2}年#{month2}月1日現在（確定値）,#{age},人口,千人,#{items[10]},#{items[11]},#{items[12]}"
            else
                csvtext += "総人口,#{year1}年#{month1}月1日現在（概算値）,#{age},人口,万人,#{items[10]},#{items[11]},#{items[12]}"
                csvtext += "総人口,#{year2}年#{month2}月1日現在（確定値）,#{age},人口,千人,#{items[3]},#{items[4]},#{items[5]}\n"
                csvtext += "日本人人口,#{year2}年#{month2}月1日現在（確定値）,#{age},人口,千人,#{items[6]},#{items[7]},#{items[8]}\n"
            end
        end
    end

    #STDERR.print csvtext
    #exit

    $firstflag = true
    CSV.parse(csvtext, headers: true).each do |row0|
        row = row0.to_h
        #pp row
        row['時点'].gsub(/(\d+)年(\d+)月1日現在（(.*)）/) do
            year = $1
            month = sprintf('%02d', $2)
            type = $3
        end
        #pp $1, $2, $3

        type = 'conf'
        if row['時点'] =~ /概算値/
            type = 'est'
        end

        if row['人口区分'] =~ /日本人人口/
            type = 'jpns'
        end
        next if row['区分'] != '人口' || ! (age = $ages[row['年齢階級']])
        $sexes.each do |k, v|
            id = "jpn_#{year}m#{month}_pop__#{type}__#{v}"
            if ! $health[id]
                $health[id] = {
                    id: id,
                    category: 'pop',
                    loc_code: 'jpn',
                    location: 'Japan',
                    date: "#{year}-#{month}-01",
                    yearmonth: "#{year}m#{month}",
                    year: year,
                    month: month,
                    sex: v,
                    type: type,
                    age_all: '', age_00_04: '', age_05_09: '', age_10_14: '', age_15_19: '', age_20_24: '', age_25_29: '', age_30_34: '', age_35_39: '', age_40_44: '', age_45_49: '', age_50_54: '', age_55_59: '', age_60_64: '', age_65_69: '', age_70_74: '', age_75_79: '', age_80_84: '', age_85_89: '', age_85over: '', age_90_94: '', age_95_99: '', age_100over: '',
                }
            end
            #pp id, age, k, row[k]
            if row['単位'] == '万人'
                $health[id][age.to_sym] = row[k].to_i * 10000
            elsif row['単位'] == '千人'
                $health[id][age.to_sym] = row[k].to_i * 1000
            end
            #pp $health[id]
       end
    end
end
